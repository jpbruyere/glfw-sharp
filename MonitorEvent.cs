namespace Glfw
{
    /// <summary>
    /// Events that may be raised from a monitor callback.
    /// </summary>
    public enum  MonitorEvent
    {
        /// <summary>
        /// The monitor was connected.
        /// </summary>
        Connected = 0x00040001,
        /// <summary>
        /// The monitor was disconnected.
        /// </summary>
        Disconnected = 0x00040002
    }
}
